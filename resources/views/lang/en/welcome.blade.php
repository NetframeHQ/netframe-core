<h2>The netframe team and all the members of netframe community welcome you!</h2>
netframe is a network that allows to share your passions and show your performances.
<ul class="netframe">
    <li>Tell us what you like to do: talk about your passions, show your performances and show your talent.</li>
    <li>Create projects and finance them.</li>
    <li>Discuss and share by creating communities.</li>
    <li>Become an angel: support a talented person that need a little help, post his performance and help him to meet his public.</li>
    <li>Organize or take part in amateur as well as professionnal projects: make donations, help financially, give your time or offer your services.</li>
    <li>Announce events and write ads.</li>
    <li>Institutions and establishments (so called "houses") are welcome to highlight their initatives, support talents and propose diverse projects : associations, bars, auditoriums, universities...</li>
</ul>
Browse on the mosaic or on the map, create playlists, react to the buzz of the moment and have a good time on netframe!