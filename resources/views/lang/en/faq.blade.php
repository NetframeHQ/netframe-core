<h2>Mosaic</h2>
<h3><a href="" data-toggle="collapse" data-target=".faq12">What is the mosaic used for? How are the photos chosen to be displayed on it?</a></h3>
<p class="collapse faq12" aria-expanded="false">
The picture-mosaic is netframe's home page. It's displaying a random selections of profiles according to your interests and the profiles' popularity.<br />
It's also the easiest way to make searches on netframe. Thanks to the bottom-left buttons, you have the possibility to display profiles according to many criteria: profile type (netframe, angel, house), talent field or category, geographical location, key-words or interests.
</p>

<h3><a href="" data-toggle="collapse" data-target=".faq13">What does the star button on profiles stand for?</a></h3>
<p class="collapse faq13" aria-expanded="false">
Click on a profile's star and you clip it on you playlist. Then you can permanently see the latest favourite media of any profile clipped in your playlist. <br />
By default, a playlist is displayed chronologically, starting from the first clip. You can, of course, organize your playlists as you wish to later.
</p>

<h2>Angels, house and communities</h2>
<h3><a href="" data-toggle="collapse" data-target=".faq14">What are the angels, houses and communities used for?</a></h3>
<p class="collapse faq14" aria-expanded="false">
If you want to help other users with their projects, you have the possibility to create an angel profile. You can then make a donation or offer financial support, put a talent in touch with an organization or a professional contact, share freely your skills. Your angel profile is also automatically generated when you complete certain actions on the website (like participating in a crowdfunding operation or a project). You then have the possibility to customize and complete your angel profile through the drag-down menu on the top-right of the screen (by clicking on your name).<br />
Bars, restaurants, concert halls, exhibition rooms and other establishments willing to help and support new talents, host their events and share their current developments, have the opportunity to create a house page on netframe.<br />
Communities allow users with a netframe profile or an angel profile to clump together according to their common interests and to discuss between them, in a public discussion or in the private thread reserved to the members of the community.
</p>
