<h2>Netframe profile</h2>
<h3><a href="" data-toggle="collapse" data-target=".faq5">What is a netframe profile? Why create a netframe profile?</a></h3>
<p class="collapse faq5" aria-expanded="false">
By subscribing to netframe, you automatically become a user. That will allow you to navigate the website, find other users' profiles, follow them and clip them on your playlist.<br />
If you want to show what you are doing, talk and exchange on the network, be visible on the map, add media, look for angels, join communities or organize projects, you need to create a netframe profile. Each netframe profile gives you the opportunity to introduce one of your passions or talents. As you probably have a lot of interests, you can make as many netframe profiles as you wish!<br />
A netframe profile allows you to be largely visible in the community to which you want to belong. You're starting rock climbing? By making your netframe profile, you could organize your climbing sessions, share your plans, meet people with the same interests and discuss with them...
</p>

<h3><a href="" data-toggle="collapse" data-target=".faq6">I want to create a netframe profile, but I don't know how to fill it in...</a></h3>
<p class="collapse faq6" aria-expanded="false">
Well, so you're a bit shy? You think you have nothing to show? It's quite understandable, but... are you really certain? Do you like to travel? Do you enjoy artistic framing? Are you keen on gardening or cooking? Are you a Star Wars nut? <br />
Yes, don't worry. Come as you are and we deal with everything else.
</p>

<h3><a href="" data-toggle="collapse" data-target=".faq7">...nor in which field to register.</a></h3>
<p class="collapse faq7" aria-expanded="false">
To make it easier for you, netframe suggests four fields and a lot of different sub-categories in a drag-down menu. You can also create your own category if you consider it doesn't exist yet.<br />
And don't worry, our search engine considers all the key words of your description. For example, if you're an actor, but only for cinema, select “actor” or “cinema” as a category, then fill your profile description in, making sure to include any key-word that will help to find you. This way anybody could find you through the search engine.<br />
If you regret your first choice, you'll be able to change it at any time by changing your netframe profile.
</p>

<h3><a href="" data-toggle="collapse" data-target=".faq8">I've created a netframe profile, but I'm not displayed on the mosaic. Why?</a></h3>
<div class="collapse faq8" aria-expanded="false">
<p>If your profile is not displayed on the home page's mosaic, it is probably for one of the following reasons: </p>
<ul>
     <li>You've not uploaded any picture or video. Only profiles with a picture or a screenshot from a video (automatically generated by a video you post) can be displayed on the mosaic ;</li>
    <li>You've created the netframe profile of a well-known personality ;</li>
    <li>If it's a photo or portrait of another person. If it's an artwork which is obviously not one of your own production (a famous painting or photography for example).</li>
</ul>
</div>

<h3><a href="" data-toggle="collapse" data-target=".faq9">I can't change my netframe profile.</a></h3>
<p class="collapse faq9" aria-expanded="false">
You have the possibility to edit your netframe profile through the drag-down menu at the top-right of the screen. After you clicked on your name, click on Talent (blue pictogram) and you should see the list of your netframe profiles in a sub-menu. Select the one you want to modify and click on the pen-shaped pictogram to edit it.<br />
You can also edit it by clicking on the gear wheel directly on your profile page.
</p>

