<p>
Le Prestataire ne saurait en aucune circonstance être responsable au titre des pertes ou dommages indirects ou imprévisibles de l'Organisation ou des tiers, ce qui inclut notamment tout gain manqué, inexactitude ou corruption de fichiers ou données, préjudice commercial, perte de chiffre d'affaires ou bénéfice, perte de clientèle ou perte de chance lié à quelque titre et sur quelque fondement que ce soit au présent Contrat. .
</p>
<p>
En cas de condamnation du Prestataire, il est expressément convenu que sa responsabilité sera limitée d'un commun accord au montant des redevances effectivement versées par l'Organisation au titre du présent Contrat.
</p>
<h4>Article 5 - Redevances</h4>
<p>
L'Organisation est redevable d'une redevance forfaitaire mensuelle ou annuelle selon l'Offre souscrite, dont le montant est fixé selon la grille tarifaire figurant à l'adresse suivante : https://www.netframe.fr/offre-reseau-social-entreprise/
</p>
<p>
La TVA pourra être éventuellement due par l'Organisation au Prestataire selon les dispositions communautaires applicables. En conséquence, l'Organisation s'engage sur simple demande du Prestataire à fournir des informations exactes et réelles quant à sa situation juridique et fiscale.
</p>
<p>
Pour une souscription mensuelle, le montant mensuel de la redevance sera versé à date anniversaire de la conclusion du présent Contrat par prélèvement bancaire automatique ; par exception, le prix mensuel du premier mois d'abonnement devra être intégralement payé au jour de la passation de la commande par carte bancaire (CB, Visa, MasterCard) ou par virement bancaire.
</p>
<p>
Pour une souscription annuelle, le prix annuel de l'abonnement devra être intégralement payé au jour de la passation de la commande selon les modalités précitées.
</p>
<p>
Les données de paiement sont échangées en mode crypté.
</p>
<p>
En cas de recours à un service tiers dans la gestion du paiement et de la facturation, le Prestataire s'assure de la sélection d'un service assurant un échange sécurisé des données de paiement. Toutefois la responsabilité du Prestataire quant à l'usage de ce service sera limité à l'engagement contractuel avec son prestataire technique et est nul vis-à-vis des tiers.
</p>
<p>
Les paiements effectués par l'Organisation ne seront considérés comme définitifs qu'après encaissement effectif des sommes dues par le Prestataire. Seul un paiement définitif entraine ouverture de l'accès à la Plateforme.
</p>
<p>
Sans préjudice d'éventuels dommages et intérêts, le défaut de paiement par l'Organisation d'une seule échéance à la date prévue entraîne de plein droit l'application d'un intérêt de retard égal à trois fois le taux d'intérêt légal, sans mise en demeure préalable et à compter du premier jour de retard auquel s'ajoutent les frais bancaires et de gestion supplémentaires. L'Organisation et les utilisateurs cesseront en outre d'accéder aux services de la Plateforme. Le Prestataire informe l'Organisation de son défaut de paiement d'une part et de la cessation d'accès d'autre part par courrier électronique.
</p>
<p>
Une facture est établie par le Prestataire et remise à l'Organisation dès le paiement réalisé.
</p>
<h4>Article 6 – Résiliation</h4>
<p>
En cas de manquement par l'une des Parties à ses obligations contractuelles, le Contrat pourra être résilié de plein droit par l'autre Partie quinze (15) jours après l'envoi d'une lettre de mise en demeure adressée par lettre recommandée avec demande d'avis de réception restée sans effet. La mise en demeure indiquera la ou les défaillances constatées.
</p>
<p>
Une fois le Contrat résilié, l'Organisation cessera d'accéder aux Services de la Plateforme et les données transmises sur la Plateforme seront détruites par le Prestataire.
</p>
