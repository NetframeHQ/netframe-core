<h2>L'équipe netframe et tous les inscrits vous souhaitent la bienvenue !</h2>

netframe est un réseau qui permet de partager ses passions et de présenter ses réalisations.
 <ul class="netframe">
    <li>Racontez-nous ce que vous aimez faire: parlez de vos passions, présentez vos créations et montrez votre talent.</li>
    <li>Créez des projets et financez-les. </li>
    <li>Engagez des discussions et des échanges en créant des communautés </li>
    <li>Devenez un angel : soutenez une personne talentueuse qui a besoin d'un coup de pouce, postez sa performance et aidez-la à trouver son public.</li>
    <li>Montez ou participez à des projets amateurs comme professionnels: faites des dons financiers ou en nature, offrez votre temps ou vos services.</li>
    <li>Annoncez des événements à venir et déposez des annonces.</li>
    <li>Les institutions et établissements (les "houses") sont les bienvenus pour valoriser leurs initiatives, accompagner des talents et proposer divers projets : associations, bars, salles de spectacle, universités... </li>
</ul>
Naviguez sur la mosaïque ou sur la carte, créez des playlists, réagissez aux buzz du moment et passez un bon moment sur netframe !",